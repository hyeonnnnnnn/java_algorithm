package sohyeon.oct15;

import java.util.Scanner;

/*
 * 문제 설명
자연수 n이 주어졌을 때, n의 다음 큰 숫자는 다음과 같이 정의 합니다.

조건 1. n의 다음 큰 숫자는 n보다 큰 자연수 입니다.
조건 2. n의 다음 큰 숫자와 n은 2진수로 변환했을 때 1의 갯수가 같습니다.
조건 3. n의 다음 큰 숫자는 조건 1, 2를 만족하는 수 중 가장 작은 수 입니다.
예를 들어서 78(1001110)의 다음 큰 숫자는 83(1010011)입니다.

자연수 n이 매개변수로 주어질 때, n의 다음 큰 숫자를 return 하는 solution 함수를 완성해주세요.
--
제한 사항
n은 1,000,000 이하의 자연수 입니다.
--
입출력 예
n	result
78	83
15	23
--
입출력 예 설명
입출력 예#1
문제 예시와 같습니다.
--
입출력 예#2
15(1111)의 다음 큰 숫자는 23(10111)입니다.
*/
public class No3 {

	public static void main(String[] args) {
		// TODO Auto-generated method stub
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		
		int answer = 0;
        
        String str = Integer.toBinaryString(n);								// n을 2진수로 변환
        int cnt = 0;														// n의 1인 비트수 저장
        
        for(int i = 0; i < str.length(); i++){
            if(str.charAt(i) == '1') cnt++;									// 1인 비트 수 카운팅
        }
        
        for(int i = n+1; i < 1000000; i++){									// n+1부터 반복
            String temp = Integer.toBinaryString(i);
            int tCnt = 0;
            
            for(int j = 0; j < temp.length(); j++){
                if(temp.charAt(j) == '1') tCnt++;
            }
            
            if(tCnt == cnt) {												// 1인 비트의 수가 일치하면 해당 수를 담아 반복문 종료
                answer = i;
                break;
            }
        }
        
        sc.close();
        System.out.println(answer);
        
	}

}
